package com.example.dreamy.Model;

public class CategoryPhake {
    public String id;
    public String mahoadon;
    public String masanpham;
    public String masize;
    public String mamau;
    public String soluong;
    public long dongia;


    public CategoryPhake() {
    }

    public CategoryPhake(String id, String mahoadon, String masanpham, String masize, String mamau, String soluong, long dongia) {
        this.id = id;
        this.mahoadon = mahoadon;
        this.masanpham = masanpham;
        this.masize = masize;
        this.mamau = mamau;
        this.soluong = soluong;
        this.dongia = dongia;
    }

}
