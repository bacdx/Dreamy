package com.example.dreamy.UI.Activity.Model;

public class CategoryPhake {
    public String id;
    public String name;
    public String price;
    public String color;
    public String size;
    public String note;
    public String much;
    public String status;
    public String img;

    public CategoryPhake() {
    }

    public CategoryPhake(String id, String name, String price, String color, String size, String note, String much, String status, String img) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.color = color;
        this.size = size;
        this.note = note;
        this.much = much;
        this.status = status;
        this.img = img;
    }
}
